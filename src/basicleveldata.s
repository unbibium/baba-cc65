.EXPORT _load_level
.EXPORT _level_array

LEVEL_TILES = LEVEL2-LEVEL1

THINGY	=	$FD ; address of level start
.CODE
;
; A: level to load
;
_load_level:
	CLC
	ROL
	TAX
	LDA _level_array,X
	STA THINGY
	INX
	LDA _level_array,X
	STA THINGY+1
	LDY #LEVEL_TILES-1
LOAD_LEVEL_LOOP:
	LDA (THINGY),Y
	STA $C000,Y
	DEY
	BNE LOAD_LEVEL_LOOP
	STA $C000
	RTS

.RODATA
